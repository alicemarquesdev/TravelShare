namespace TravelShare.Enums
{
    // Enum que define os tipos de notificações que um usuário pode receber no sistema.
    public enum NotificacaoEnum
    {
        // Notificação quando um usuário curte um post.
        CurtidaPost = 0,

        // Notificação quando um usuário comenta em um post.
        ComentarioPost = 1,

        // Notificação quando um usuário começa a seguir outro usuário.
        SeguiuVoce = 2
    }
}
